typedef unsigned long	size_t;
typedef signed long		ssize_t;
typedef signed long		intptr_t;

void	*syscall3(intptr_t n, void *a1, void *a2, void *a3);

static inline ssize_t	ft_write(int fd, const void *buf, size_t n) {
	return (intptr_t)syscall3(1, (void *)(intptr_t)fd, (void *)buf, (void *)n);
}

ssize_t	ft_putchar(const char c) {
	return ft_write(1, &c, 1);
}
